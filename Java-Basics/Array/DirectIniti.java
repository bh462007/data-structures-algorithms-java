class DirectInili{
    public static void main(String[] args) {
        int[] num={2, 4, 6, 8};
        System.out.println("Element at 2nd index:"+num[2]);
        System.out.println("Element at last index: "+num[num.length-1]);
        for (int i = 0; i < num.length; i++) {
            System.out.println(" "+num[i]);
        }
        
    }
}